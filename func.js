function eatShit(num1, num2) {
	return num1 + num2;
}

function drinkWater(num1, num2) {
	return num1 * num2;
}

function killBears(num1, num2) {
	return num1 / num2;
}

function peelBanana(num1, num2) {
	return num1 - num2;
}

module.exports = {
	eatShit: eatShit,
	drinkWater: drinkWater,
	killBears: killBears,
	peelBanana: peelBanana
}